pub fn add_fuzzers<'a>(session: &mut Session<IseBackend<'a>>, backend: &IseBackend<'a>) {
    let ExpandedNamedDevice::Virtex4(ref endev) = backend.endev else {
        unreachable!()
    };
    for dir in [Dir::S, Dir::N] {
        let Some(ref ngt) = endev.gtz[dir] else {
            continue;
        };
        let ctx = FuzzCtx::new_fake_tile(
            session,
            backend,
            "GTZ",
            "GTZ",
            TileBits::Raw(vec![BitTile::Gtz(dir)]),
        );
        fuzz_one!(ctx, "PRESENT", "1", [], [
            (raw Key::SiteMode(&ngt.bel), None, "GTZE2_OCTAL")
        ]);
        for &pin in GTZ_INVPINS {
            fuzz_one!(ctx, format!("INV.{pin}"), "0", [
                (raw Key::SiteMode(&ngt.bel), "GTZE2_OCTAL"),
                (raw Key::SitePin(&ngt.bel, pin.into()), true)
            ], [
                (raw Key::SiteAttr(&ngt.bel, format!("{pin}INV")), None, pin)
            ]);
            fuzz_one!(ctx, format!("INV.{pin}"), "1", [
                (raw Key::SiteMode(&ngt.bel), "GTZE2_OCTAL"),
                (raw Key::SitePin(&ngt.bel, pin.into()), true)
            ], [
                (raw Key::SiteAttr(&ngt.bel, format!("{pin}INV")), None, format!("{pin}_B"))
            ]);
        }
        for &attr in GTZ_BOOL_ATTRS {
            for val in ["FALSE", "TRUE"] {
                fuzz_one!(ctx, attr, val, [
                    (raw Key::SiteMode(&ngt.bel), "GTZE2_OCTAL")
                ], [
                    (raw Key::SiteAttr(&ngt.bel, attr.to_string()), None, val)
                ]);
            }
        }
        for &(attr, vals) in GTZ_ENUM_ATTRS {
            for &val in vals {
                fuzz_one!(ctx, attr, val, [
                    (raw Key::SiteMode(&ngt.bel), "GTZE2_OCTAL")
                ], [
                    (raw Key::SiteAttr(&ngt.bel, attr.to_string()), None, val)
                ]);
            }
        }
        for &(attr, width) in GTZ_BIN_ATTRS {
            fuzz_multi!(ctx, attr, "", width, [
                (raw Key::SiteMode(&ngt.bel), "GTZE2_OCTAL")
            ],
                (raw Key::SiteAttr(&ngt.bel, attr.to_string()), MultiValue::Bin)
            );
        }
        for &(attr, width) in GTZ_HEX_ATTRS {
            fuzz_multi!(ctx, attr, "", width, [
                (raw Key::SiteMode(&ngt.bel), "GTZE2_OCTAL")
            ],
                (raw Key::SiteAttr(&ngt.bel, attr.to_string()), MultiValue::Hex(0))
            );
        }
    }
}

pub fn collect_fuzzers(ctx: &mut CollectorCtx) {
    let ExpandedDevice::Virtex4(ref edev) = ctx.edev else {
        unreachable!()
    };
    if edev.gtz.values().any(|x| x.is_some()) {
        let tile = "GTZ";
        let bel = "GTZ";
        ctx.state.get_diff(tile, bel, "PRESENT", "1").assert_empty();
        for &pin in GTZ_INVPINS {
            ctx.collect_enum_bool(tile, bel, &format!("INV.{pin}"), "0", "1");
        }
        for &attr in GTZ_BOOL_ATTRS {
            ctx.collect_enum_bool(tile, bel, attr, "FALSE", "TRUE");
        }
        for &(attr, vals) in GTZ_ENUM_ATTRS {
            ctx.collect_enum_ocd(tile, bel, attr, vals, OcdMode::BitOrder);
        }
        for &(attr, _) in GTZ_BIN_ATTRS {
            ctx.collect_bitvec(tile, bel, attr, "");
        }
        for &(attr, _) in GTZ_HEX_ATTRS {
            ctx.collect_bitvec(tile, bel, attr, "");
        }
    }
}
